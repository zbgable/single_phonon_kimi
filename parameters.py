"""
物理参数配置文件
================
单位系统：GHz（频率）、ns（时间），自然单位制 ℏ = 1
"""

import numpy as np

# ============================================================================
# Transmon Qubit 参数
# ============================================================================

# Qubit 跃迁频率 (GHz)
# 典型值 4-6 GHz，与微波腔和声子波导匹配
OMEGA_Q = 5.0  

# Transmon 非谐性 (GHz)
# 负值表示能级向下弯曲，α = E_C
# 典型值 0.2-0.4 GHz
ALPHA = -0.3

# ============================================================================
# 耗散与退相干参数
# ============================================================================

# Qubit 固有弛豫率 T1^{-1} (GHz)
# T1 = 1 μs → γ1 = 0.001 GHz
GAMMA_1 = 0.001

# 纯退相率 (GHz)
# Tφ = 2 μs → γφ = 0.0005 GHz  
# 注意：γφ 定义在 Lindblad 算符 σ_z ρ σ_z - ρ 中
# 对应的退相时间 T2 满足：1/T2 = 1/(2*T1) + γφ
GAMMA_PHI = 0.0005

# 向行波声子波导的发射率 (GHz)
# 这是有用的耗散通道，决定声子发射效率
# 通过 IDT 耦合强度控制，典型值 0.01-0.1 GHz
KAPPA_PH = 0.02

# ============================================================================
# 微波驱动参数
# ============================================================================

# 驱动频率 (GHz)，通常等于或接近 qubit 频率
# 在旋转框架中设为 OMEGA_Q 消除快速振荡
OMEGA_D = OMEGA_Q

# 驱动幅度 Ω_0 (GHz)
# π-pulse 时间 t_π ≈ π / Ω_0
# 例：Ω_0 = 0.1 GHz → t_π ≈ 31.4 ns
OMEGA_0 = 0.1

# ============================================================================
# 脉冲参数
# ============================================================================

# 脉冲类型: 'gaussian' 或 'sech'
PULSE_TYPE = 'sech'  # 双曲正割波包

# 脉冲宽度 σ (ns)
# 高斯: Δω ≈ 1/σ
# sech: Δω ≈ 1/(πσ/2) = 2/(πσ)
SIGMA = 20.0  # ns

# 脉冲中心位置 t_0 (ns)
PULSE_CENTER_1 = 50.0   # 第一个脉冲中心
PULSE_CENTER_2 = 150.0  # 第二个脉冲中心（Time-bin 编码用）

# 脉冲间隔 Δt (ns)
# 用于 Time-bin 编码，需满足 Δt >> σ 以确保时序分离
PULSE_SEPARATION = 100.0

# ============================================================================
# 仿真时间设置
# ============================================================================

# 总仿真时间 (ns)
T_MAX = 300.0

# 时间步数（影响精度和速度）
N_STEPS = 500

# 时间数组
T_LIST = np.linspace(0, T_MAX, N_STEPS)

# ============================================================================
# 计算辅助参数
# ============================================================================

# 总衰减率（所有 Lindblad 项之和）
# 用于估算系统演化特征时间
GAMMA_TOTAL = GAMMA_1 + KAPPA_PH

# 纯退相导致的有效 T2 时间 (ns)
T2_STAR = 1.0 / (0.5 * GAMMA_1 + GAMMA_PHI)

# 理论最大量子效率（稳态）
# η_max = κ_ph / (γ1 + κ_ph)
# 这表示 qubit 衰减能量转化为声子的最大比例
ETA_MAX = KAPPA_PH / (GAMMA_1 + KAPPA_PH)

# ============================================================================
# 可视化参数
# ============================================================================

# 图形 DPI
DPI = 150

# 颜色方案
COLOR_POPULATION = '#2E86AB'    # 蓝色：布居数
COLOR_EFFICIENCY = '#A23B72'    # 紫红色：效率
COLOR_CUMULATIVE = '#F18F01'    # 橙色：累计声子数
COLOR_PULSE = '#C73E1D'         # 红色：脉冲包络

print("=" * 60)
print("Transmon-声子发射仿真参数")
print("=" * 60)
print(f"Qubit 频率: ω_q = {OMEGA_Q} GHz")
print(f"非谐性: α = {ALPHA} GHz")
print(f"固有弛豫: γ1 = {GAMMA_1*1000} MHz (T1 = {1/GAMMA_1:.1f} μs)")
print(f"纯退相: γφ = {GAMMA_PHI*1000} MHz (Tφ = {1/GAMMA_PHI:.1f} μs)")
print(f"声子发射率: κ_ph = {KAPPA_PH*1000} MHz")
print(f"理论最大效率: η_max = {ETA_MAX:.1%}")
print(f"高斯脉冲宽度: σ = {SIGMA} ns")
print(f"π-pulse 时间: t_π ≈ {np.pi/OMEGA_0:.1f} ns")
print("=" * 60)
